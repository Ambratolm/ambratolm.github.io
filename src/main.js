//====================================================================================================
// @ Main
//----------------------------------------------------------------------------------------------------
// 		Application main entry point.
//====================================================================================================

//----------------------------------------------------------------------------------------------------
// # CSS Style
//----------------------------------------------------------------------------------------------------
import "@fortawesome/fontawesome-free/css/all.css";
import "animate.css";
import "hover.css";
import "halfmoon/css/halfmoon.css";
import "halfmoon/css/cores/halfmoon.cores.css";
import "./style.css";

//----------------------------------------------------------------------------------------------------
// # JS Style
//----------------------------------------------------------------------------------------------------
import "bootstrap/js/dist/collapse";
import "bootstrap/js/dist/dropdown";

//----------------------------------------------------------------------------------------------------
// # Application
//----------------------------------------------------------------------------------------------------
import { createApp } from "vue";
import App from "./App.vue";
const app = createApp(App);

//----------------------------------------------------------------------------------------------------
// # Store
//----------------------------------------------------------------------------------------------------
import { createPinia } from "pinia";
const pinia = createPinia();
app.use(pinia);

//----------------------------------------------------------------------------------------------------
// # Router
//----------------------------------------------------------------------------------------------------
import routes from "./routes";
import { createWebHistory, createRouter } from "vue-router";
const router = createRouter({
  linkActiveClass: "active",
  linkExactActiveClass: "active",
  history: createWebHistory(),
  routes,
});
app.use(router);

//----------------------------------------------------------------------------------------------------
// # Masonry
//----------------------------------------------------------------------------------------------------
import MasonryWall from "@yeger/vue-masonry-wall";
app.use(MasonryWall);

//----------------------------------------------------------------------------------------------------
// # Global Properties
//----------------------------------------------------------------------------------------------------
import { useCssLibrary } from "./modules/utilities";
app.config.globalProperties.$navRoutes = routes.filter(
  (route) => route.meta?.isNav,
);
app.config.globalProperties.$animate = (classes, event) =>
  useCssLibrary(`animated ${classes}`, "animate__", event?.target);
//----------------------------------------------------------------------------------------------------
// # Global Directives
//----------------------------------------------------------------------------------------------------
import { imgSrcAlt, tooltip } from "@/modules/directives";
app.directive("tooltip", tooltip);
app.directive("imgSrcAlt", imgSrcAlt);

//----------------------------------------------------------------------------------------------------
// # Application Boot
//----------------------------------------------------------------------------------------------------
app.mount("#app");
